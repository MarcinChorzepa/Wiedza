public class Calculator {
    double add(double a,double b){
        double result =a+b;
        return result;
    }
    double multipy(double a,double b){
        return a*b;
    }
    double substract(double a, double b){
        return a-b;
    }
    double divide(double a, double b){
        return a/b;
    }

}
